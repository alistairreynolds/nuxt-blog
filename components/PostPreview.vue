<template>
  <nuxt-link :to="link">
    <article class="post-preview">
      <div class="post-thumbnail" :style="{backgroundImage:`url('/images/${thumb}')`}" />
      <div class="post-content">
        <h2>{{ title }}</h2>
        <p>{{ previewText }}</p>
      </div>
    </article>
  </nuxt-link>
</template>

<script>
export default {
  name: 'PostPreview',
  props: {
    isAdmin: {
      type: Boolean,
      required: true
    },
    id: {
      type: Number,
      required: true
    },
    title: {
      type: String,
      required: true
    },
    previewText: {
      type: String,
      required: true
    },
    thumb: {
      type: String,
      required: true
    }
  },
  computed: {
    link () {
      return this.isAdmin ? `/admin/${this.id}` : `/posts/${this.id}`
    }
  }
}

</script>

